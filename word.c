/*
	:用户指定在开发板的合适位置显示一个字符。
	@x0,y0:用户指定的显示位置。
	@word[]:用户指定显示的字符
	@w,h:显示字符的大小。
		字符大小取决于取模的字体大小，就是宽度和高度
*/
void word_display(char word[],int x0,int y0,int w,int h)
{
	//1.找到该像素点的显示位置
	int dian; //表示当前判断的这个像素点
	for(dian = 0;dian < w * h;dian++)
	{
		//要在屏幕上合适的位置显示这个像素点
		
		int x,y;//这个dian像素点，在屏幕上的显示位置
		
		//dian这个像素点在原始字符的第 dian / w行
		y = y0 + dian / w;
		
		//dian这个像素点在原始字符的第 dian % w列
		x = x0 + dian % w;
		
		//dian这个像素点是否在字符上。
		//判断依据:取模之后对应的那一个bit位是1还是0
		int index = dian / 8;	//求出当前像素点在取模数据中的第几个元素中
		
		//word[index]这个元素一共有8个bit位，当前dian对应的是哪一位？
		int bit = 7 - dian % 8;
		
		int color;//当前像素点要显示的颜色
		
		if(word[index] & (1<<bit))
		{
			//当前像素点在字符上，对应bit位为1
			color = 0x000000;
		}
		else
		{
			color = 0xFFEFD5;
		}
		display_point(x,y,color);
	}
}

void show_tem(double temperature, int center_x, int center_y)
{
    int w = 16;      // 每个字符宽度
    int h = 16;      // 每个字符高度
    int spacing = 4; // 字符间距
    int x, y;

    // 1️⃣ 判断是否为负数
    int is_negative = 0;
    if (temperature < 0) {
        is_negative = 1;
        temperature = -temperature;
    }
	printf("5");
    // 2️⃣ 分离整数与小数
    int integer = (int)temperature;
    int decimal = (int)((temperature - integer) * 10);

    // 3️⃣ 计算整数部分字符个数
    char buf[10];
    sprintf(buf, "%d", integer);
    int num_len = 0;
    for (int i = 0; buf[i] != '\0'; i++) num_len++;
	printf("5");
    // 4️⃣ 总字符数统计
    int char_count = num_len + 1 /*小数点*/ + 1 /*小数位*/ + 2 /*°C*/ + (is_negative ? 1 : 0);

    // 5️⃣ 计算总宽度，实现居中
    int total_width = char_count * w + (char_count - 1) * spacing;
    int x0 = center_x - total_width / 2;
    int y0 = center_y - h / 2;

    x = x0;
    y = y0;
	printf("5");
    // 6️⃣ 显示负号
    if (is_negative) {
        word_display(word[12], x, y, w, h);
        x += w + spacing;
    }
	printf("5");
    // 7️⃣ 显示整数部分
    for (int i = 0; buf[i] != '\0'; i++) {
        int num = buf[i] - '0';
        word_display(word[num], x, y, w, h);
        x += w + spacing;
    }
	printf("5");
    // 8️⃣ 显示小数点（自己画一个3x3的点）
    int dot_x = x + w / 3;
    int dot_y = y + h - 4;
    for (int i = 0; i < 3; i++)
        for (int j = 0; j < 3; j++)
            display_point(dot_x + i, dot_y + j, 0x000000);
    x += w / 2;
	printf("5");
    // 9️⃣ 显示小数位
    word_display(word[decimal], x, y, w, h);
    x += w + spacing;
	printf("5");
    // 🔟 显示 “°”
    word_display(word[10], x, y, w, h);
    x += w + spacing;
	printf("5");
    // 11️⃣ 显示 “C”
    word_display(word[11], x, y, w, h);
}